// -- while loop --

let index = 0
while(index <=10){
    console.log(`Value of index is ${index}`);
    index += 2;
}


let myArray = ["flash", "bataman", "superman"]
let arrIndex = 0
while(arrIndex < myArray.length){
    console.log(`Value is ${myArray[arrIndex]}`);
    arrIndex++
}


// -- do while loop --

let score = 1
do{
    console.log(`Score is ${score}`);
    score++
}
while(score <= 10)

let num = 11
do{
    console.log(`Num is ${num}`);   /// special condition of do while.
    num++
}
while(num<=10)